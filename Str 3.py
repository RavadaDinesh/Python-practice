# Modified String

# Upper case
a = "hello, world"
print(a.upper())

# Lower case
b = "HELO, WORLD"
print(b.lower())

# Remove Whitespace
# strip() is the keyword
c = " Hello, World "
print(c.strip()) 

# Replace()
d = "hello, world"
print(d.replace("h", "B"))

# Split string
e = "hello, world"
print(e.split(","))

